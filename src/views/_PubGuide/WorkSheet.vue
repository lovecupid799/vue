<script setup>
import { ref, onMounted } from 'vue'

const anchorList = ref([
  { id: 'COM', name: 'Common' },
  { id: 'MENU1', name: 'Menu1' },
  { id: 'MENU2', name: 'Menu2' },
  { id: 'MENU3', name: 'Menu3' },
  { id: 'MENU4', name: 'Menu4' },
])

const menuSelectedIndex = ref(null)

const onAnchorClick = (event, menu, index) => {
  const $target = `#${menu.id}`
  const _offset = document.querySelector($target).offsetTop + 102
  window.scrollTo({ top: _offset, left: 0 })
  menuSelectedIndex.value = index
}

let $anchor = []

onMounted(() => {
  const _headH = document.querySelector('.work-head').offsetHeight
  anchorList.value.forEach((e, idx) => {
    $anchor[idx] = document.querySelector(`#${e.id}`).offsetTop + _headH
  })

  const scrollAct = () => {
    const isScrollTop = window.scrollY
    let bottomOffset = 10
    const scrolledToBottom =
      Math.ceil(isScrollTop + window.innerHeight) >=
      document.documentElement.scrollHeight - bottomOffset

    $anchor.forEach((e, idx) => {
      let num = $anchor.length - 1
      if ($anchor[idx] < isScrollTop) {
        console.log(`info : ${$anchor[idx]},${isScrollTop}`)
        scrolledToBottom && idx !== num
          ? (menuSelectedIndex.value = num)
          : (menuSelectedIndex.value = idx)
      } else if (isScrollTop < 100) {
        menuSelectedIndex.value = null
      }
    })
  }
  window.addEventListener('scroll', scrollAct)
})
</script>

